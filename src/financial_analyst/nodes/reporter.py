def report_generator(state: AnalystState) -> dict:
    """
    LangGraph node — final node in the graph.
    Transforms raw analysis into a polished markdown report.

    Reads:  state["analysis"], state["structured_data"],
            state["critique"], state["citations"]
    Writes: state["final_report"], state["citations"]
    """
    analysis = state.get("analysis", "").strip()
    structured_data = state.get("structured_data", {})
    critique_text = state.get("critique", "")

    company_name = structured_data.get("company_name") or "Unknown Company"
    ticker = structured_data.get("ticker") or "N/A"
    period = structured_data.get("period") or "Unknown Period"
    date = datetime.now().strftime("%B %d, %Y")

    if not analysis:
        return {
            "final_report": "# Report Generation Failed\n\nNo analysis was produced.",
            "errors": ["report_generator: received empty analysis."],
        }

    logger.info(f"Generating final report for {company_name} ({ticker})")

    try:
        llm = _get_llm()
        chain = REPORTER_PROMPT | llm | StrOutputParser()

        final_report = _invoke_with_retry(chain,{
            "company_name": company_name,
            "ticker": ticker,
            "period": period,
            "date": date,
            "structured_data": _build_metrics_context(structured_data),
            "analysis": analysis,
            "critique": critique_text or "No critique available.",
        })

        # Build citations and append to report
        citations = _build_citations(state)
        if citations:
            citations_block = "\n\n---\n\n## Data Sources\n" + "\n".join(
                f"- {c}" for c in citations
            )
            # Only append if not already in report
            if "## Data Sources" not in final_report:
                final_report += citations_block

        logger.info(f"Report generated — {len(final_report)} chars")

        return {
            "final_report": final_report,
            "citations": citations,
            "errors": [],
        }

    except Exception as e:
        logger.error(f"report_generator failed: {e}")
        # Fallback — return raw analysis as the report
        fallback = f"# {company_name} Analysis\n\n{analysis}"
        return {
            "final_report": fallback,
            "errors": [f"report_generator formatting failed: {str(e)}"],
        }