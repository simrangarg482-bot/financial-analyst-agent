def _build_metrics_context(data: dict) -> str:
    """Formats structured data into a clean context block for the reporter."""
    if not data:
        return "No structured data available."

    lines = []
    inc = data.get("income_statement", {})
    bal = data.get("balance_sheet", {})
    met = data.get("key_metrics", {})

    metric_map = {
        "Revenue": (inc.get("revenue"), "${:,.1f}M"),
        "Revenue Growth YoY": (inc.get("revenue_growth_yoy"), "{:.1f}%"),
        "Gross Margin": (inc.get("gross_margin"), "{:.1f}%"),
        "Operating Income": (inc.get("operating_income"), "${:,.1f}M"),
        "Net Income": (inc.get("net_income"), "${:,.1f}M"),
        "Diluted EPS": (inc.get("eps_diluted"), "${:.2f}"),
        "EBITDA": (inc.get("ebitda"), "${:,.1f}M"),
        "Cash": (bal.get("cash_and_equivalents"), "${:,.1f}M"),
        "Total Debt": (bal.get("total_debt"), "${:,.1f}M"),
        "Debt/Equity": (bal.get("debt_to_equity"), "{:.2f}x"),
        "P/E Ratio": (met.get("pe_ratio"), "{:.1f}x"),
        "ROE": (met.get("roe"), "{:.1f}%"),
        "Free Cash Flow": (met.get("free_cash_flow"), "${:,.1f}M"),
    }

    for label, (value, fmt) in metric_map.items():
        if value is not None:
            try:
                lines.append(f"{label}: {fmt.format(value)}")
            except (ValueError, TypeError):
                lines.append(f"{label}: {value}")

    return "\n".join(lines) if lines else "No metrics available."


def _build_citations(state: AnalystState) -> list[str]:
    """
    Builds a citations list from state.
    Combines any citations accumulated during ingestion
    with the input source reference.
    """
    citations = list(state.get("citations", []))
    input_type = state.get("input_type", "text")
    user_input = state.get("user_input", "")

    if input_type == "pdf":
        citations.insert(0, f"Primary source: {user_input} (PDF filing)")
    elif input_type == "ticker":
        citations.insert(0, f"Primary source: {user_input} — SEC EDGAR + Tavily search")
    elif input_type == "url":
        citations.insert(0, f"Primary source: {user_input}")
    else:
        citations.insert(0, "Primary source: User-provided text")

    # Deduplicate while preserving order
    seen = set()
    unique = []
    for c in citations:
        if c not in seen:
            seen.add(c)
            unique.append(c)

    return unique


